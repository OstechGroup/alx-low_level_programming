#include "main.h"
#include <string.h>

/**
 * _strcmp - takes two strings and returns an integer after comparison
 * @s1: represents the first string
 * @s2: represents the second string
 * Return: returns an integer based on comparison
 */

int _strcmp(char *s1, char *s2)
{
int cmp = strcmp(s1, s2);
return (cmp);
}
